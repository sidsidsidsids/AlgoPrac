import sys

M, N = map(int, sys.stdin.readline().rstrip('\n').split())

# 회전수
# 0 0 0 0 0 0 0 0
# 1 2 2 2 2 2 2 2
# 1 3 4 4 4 4 4 4
# 1 3 5 6 6 6 6 6
# 1 3 5 7 8 8 8 8
# 1 3 5 7 9 10 10 10
# 1 3 5 7 9 11 12 12

minima = min(M, N)
turn = 2 * (minima - 1)
if M > N:
    turn += 1

# 종료 좌표
# (1,1) (1,2) (1,3) (1,4) (1,5) (1,6) (1,7) (1,8)
# (2,1) (2,1) (2,1) (2,1) (2,1) (2,1) (2,1) (2,1)
# (3,1) (2,1) (2,2) (2,3) (2,4) (2,5) (2,6) (2,7)
# (4,1) (2,1) (3,2) (3,2) (3,2) (3,2) (3,2) (3,2)
# (5,1) (2,1) (4,2) (3,2) (3,3) (3,4) (3,5) (3,6)
# (6,1) (2,1) (5,2) (3,2) (4,3) (4,3) (4,3) (4,3)
# (7,1) (2,1) (6,2) (3,2) (5,3) (4,3) (4,4) (4,5)
# (8,1) (2,1) (7,2) (3,2) (6,3) (4,3) (5,4) (5,4)

y = int
x = int
half = minima // 2
if minima % 2:
    if M == N:
        y = half + 1
        x = half + 1
    elif M > N:
        y = M - half
        x = half + 1
    elif M < N:
        y = half + 1
        x = N - half
else:
    y = half + 1
    x = half

print(turn)
print(y, x)
